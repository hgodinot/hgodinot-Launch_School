class PokerHand
  attr_accessor :hand
  attr_reader :values
  
  def initialize(deck)
    @hand = []
    5.times { hand << deck.draw }
    hand.sort!
    @values = hand.map { |card| card.value }
    @suits = hand.map { |card| card.suit }
  end

  def print
    hand.each { |card| puts card }
  end

  def evaluate
    if royal_flush?     then 'Royal flush'
    elsif straight_flush?  then 'Straight flush'
    elsif four_of_a_kind?  then 'Four of a kind'
    elsif full_house?      then 'Full house'
    elsif flush?           then 'Flush'
    elsif straight?        then 'Straight'
    elsif three_of_a_kind? then 'Three of a kind'
    elsif two_pair?        then 'Two pair'
    elsif pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def royal_flush?
    straight_flush? && hand.last.rank == "Ace"
  end

  def straight_flush?
    straight? && flush?
  end

  def four_of_a_kind?
    values.count(values.first) == 4 || values.count(values.last) == 4
  end

  def full_house?
    values.uniq.size == 2
  end

  def flush?
    @suits.uniq.size == 1
  end

  def straight?
    values.uniq == values && values.last - values.first == 4
  end

  def three_of_a_kind?
    values.count(values.first) == 3 || values.count(values.last) == 3
  end

  def two_pair?
    values.uniq.size == 3
  end

  def pair?
    values.uniq.size == 4
  end
end

class Deck

  RANKS = ((2..10).to_a + %w(Jack Queen King Ace)).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze
  
  def initialize
    @deck = []
    reset
  end
  
  def draw
    reset if @deck.empty?
    @deck.pop
  end
  
  def reset
    RANKS.product(SUITS).shuffle.map { |rank, suit| @deck << Card.new(rank, suit)}
  end
  
end


class Card
  include Comparable
  attr_reader :rank, :suit
  
  CONVERSION = {'Jack' => 11, 'Queen' => 12, 'King' => 13, 'Ace' => 14}

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end
  
  def value
    return rank if (2..10).include?(rank)
    CONVERSION[rank]
  end
  
  def to_s
    "#{rank} of #{suit}"
  end
  
  def <=>(other_card)
    value <=> other_card.value
  end
end

